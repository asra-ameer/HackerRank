import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

public class Solution {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
         int i = 1;

         while(sc.hasNext()){
              String line= sc.nextLine();
              System.out.print(i+" "+line);
              i++;
              System.out.println();
          }

    }
}
